        <p><h3><strong>購入代行手数料について</strong></h3>
          購入代行サービスの手数料は代行手数料とオプション手数料があります。<br>
          購入代行手数料は商品購入代金を基準として計算されています。<br>
          商品購入代金は、ショップへ支払う総額（商品金額＋送料＋振込み手数料など）になります。
          <br><br><br><br>

          <h3><strong>代行手数料</strong></h3>
          代行手数料はショップに支払う件数分発生します。<br>
          １回に複数の商品を依頼する時に購入するショップが複数になるとショップに支払う件数分、それぞれの代行手数料を計算します。<br>
          例）<br>
          ２種類の商品を同じショップで購入＝１件の代行手数料 <br>
          ２種類の商品をそれぞれ異なるショップで購入＝２件の代行手数料<br>
          ２種類の商品を同じショップで購入、決済が２回必要＝２件の代行手数料<br>
          同じショップで購入した複数商品を複数の配送先へ発送＝発送先件数分の代行手数料<br>
          <span style="color:red">商品購入代金 + 国際送料 + 手数料 = 請求金額</span><br>
          ※商品購入代金は購入代行サービスの申込後、商品購入前まで決済を済ませてください。<br>
          ※(国際送料+代行手数料)は商品の発送準備が完了後、２次請求書をお送りしますので２次請求書確認の上、決済を済ませてください。<br>
